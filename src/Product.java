

class Product {

  private double price;
  private  double weight;

  public Product(double price, double weight) {
    this.price = price;
    this.weight = weight;
  }

  public double getPrice() {
    return price;
  }

  public double getWeight() {
    return weight;
  }

  public void setPrice(double price) {
    this.price = price;
  }

  public void setWeight(double weight) {
    this.weight = weight;
  }


}